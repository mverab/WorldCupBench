async function loadData() {
  try {
    const [lb, tn, ps, rs, br] = await Promise.all([
      fetch('data/leaderboard.json').then(r => r.ok ? r.json() : null).catch(() => null),
      fetch('data/tournament.json').then(r => r.ok ? r.json() : null).catch(() => null),
      fetch('data/predictions_summary.json').then(r => r.ok ? r.json() : null).catch(() => null),
      fetch('data/results.json').then(r => r.ok ? r.json() : null).catch(() => null),
      fetch('data/bracket.json').then(r => r.ok ? r.json() : null).catch(() => null),
    ]);
    leaderboard = lb;
    tournament = tn;
    predictionsSummary = ps;
    bracketData = br;

    resultsByMatchId = {};
    if (rs) {
      const list = Array.isArray(rs) ? rs : (rs.matches || []);
      for (const m of list) {
        if (m.match_id != null) {
          resultsByMatchId[String(m.match_id)] = m;
        }
      }
    }
  } catch (e) {
    console.error('Data load error:', e);
  }
  render();
}

let disagreementData = null;

function filterDisagreementByPhase(matches, phase) {
  if (phase === 'all') return matches;
  return matches.filter(m => m.phase === phase);
}

async function loadDisagreement(phase = 'all') {
  const el = document.getElementById('disagreement-content');

  // Serve from cache once the static file has been loaded.
  if (disagreementData) {
    renderDisagreement(filterDisagreementByPhase(disagreementData.matches, phase), phase);
    return;
  }

  el.innerHTML = '<p class="text-gray-500 text-center py-12">Loading...</p>';
  try {
    // Static JSON generated by scripts/generate_disagreement.py (GitHub Pages friendly).
    let data = await fetch('data/disagreement.json')
      .then(r => (r.ok ? r.json() : null))
      .catch(() => null);

    // Fall back to the live API server if the static file is unavailable.
    if (!data) {
      const url = new URL(`${API_BASE_URL}/api/disagreement`);
      const response = await fetch(url);
      if (!response.ok) throw new Error(`HTTP ${response.status}`);
      data = await response.json();
    }

    disagreementData = data;
    renderDisagreement(filterDisagreementByPhase(data.matches || [], phase), phase);
  } catch (e) {
    el.innerHTML = `<p class="text-red-400 text-center py-12">Could not load disagreement data.</p>`;
    console.error('Disagreement load error:', e);
  }
}

